/*1.Reutilização de código
Modularização permite que partes do código sejam usadas em diferentes
partes do projeto sem precisar reescrevê-las. Por exemplo, uma função de 
cálculo de impostos pode ser usada em várias telas de um sistema de vendas.

2.Facilidade de manutenção
Com o código dividido em módulos menores, é mais fácil corrigir erros e fazer
melhorias sem afetar o restante do sistema. Se um módulo de login precisar de ajustes, 
ele pode ser alterado sem impactar outras partes do site.

3.Melhor organização do código
Separar o código em módulos torna o projeto mais organizado, facilitando a leitura
e o entendimento por outros desenvolvedores. Por exemplo, um sistema de e-commerce
pode ter módulos separados para "pagamento", "estoque" e "usuários", deixando o código mais claro.*/